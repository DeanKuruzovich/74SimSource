// ── 74Sim Onramp Lesson Diagrams ─────────────────────────────────────────────
// Every inline SVG diagram used by the lesson content, in one place.
//
// DESIGN SYSTEM (keep new diagrams consistent — helperscripts/diagram-lint.mjs
// enforces most of this):
//
//   Colors are GRAYSCALE ONLY, on the #161616 page background:
//     INK   #e8e8e8  primary: signal names, key values, emphasized strokes
//     DIM   #9a9a9a  secondary: titles, captions, annotations, gate names
//     WIRE  #c0c0c0  default signal lines and symbol outlines
//     FAINT #5c5c5c  inactive lines, dashed guides, tick axes
//     BOXLINE #525252 / BOXFILL #1d1d1d  component boxes
//   Emphasis is carried by BRIGHTNESS + STROKE WIDTH, never hue:
//     the important path is INK at width 2+, ordinary wires are WIRE at 1.5,
//     background/inactive elements are FAINT.
//
//   Geometry:
//     - Arrows are generated by arrow()/head(): the head tip lands EXACTLY on
//       the line's endpoint, the shaft stops at the head's base.
//     - Title: centered, y=11, size 8, DIM.  Captions: centered at the bottom,
//       size 8, DIM, 12px line pitch — keep each caption line ≤ 58 chars so
//       it fits a 260-wide viewBox (≤ 68 chars for 300-wide).
//     - Keep ≥ 4px clearance between any text and any line/arrowhead.

const FONT = 'Roboto, Arial, sans-serif';

export const INK = '#e8e8e8';
export const DIM = '#9a9a9a';
export const WIRE = '#c0c0c0';
export const FAINT = '#5c5c5c';
export const BOXLINE = '#525252';
export const BOXFILL = '#1d1d1d';
export const PALETTE = [INK, DIM, WIRE, FAINT, BOXLINE, BOXFILL, '#161616', 'none'];

const r1 = (v) => Math.round(v * 10) / 10;

export function txt(x, y, s, o = {}) {
  const { size = 8, fill = INK, anchor = 'start', rotate = null } = o;
  const tr = rotate ? ` transform="rotate(${rotate} ${x} ${y})"` : '';
  return `<text x="${x}" y="${y}" fill="${fill}" font-size="${size}" font-family="${FONT}" text-anchor="${anchor}"${tr}>${s}</text>`;
}

export function line(x1, y1, x2, y2, o = {}) {
  const { stroke = WIRE, w = 1.5, dash = null, cap = null } = o;
  const d = dash ? ` stroke-dasharray="${dash}"` : '';
  const c = cap ? ` stroke-linecap="${cap}"` : '';
  return `<line x1="${x1}" y1="${y1}" x2="${x2}" y2="${y2}" stroke="${stroke}" stroke-width="${w}"${d}${c}/>`;
}

export function poly(points, o = {}) {
  const { stroke = WIRE, w = 1.5, dash = null } = o;
  const d = dash ? ` stroke-dasharray="${dash}"` : '';
  return `<polyline points="${points}" fill="none" stroke="${stroke}" stroke-width="${w}"${d}/>`;
}

// Solid triangular arrowhead with its tip exactly at (x,y), pointing along
// (ux,uy). Standard size: 7 long, 6.4 wide at the base.
export function head(x, y, dir, o = {}) {
  const { fill = WIRE, len = 7 } = o;
  const dirs = { right: [1, 0], left: [-1, 0], up: [0, -1], down: [0, 1] };
  let [ux, uy] = Array.isArray(dir) ? dir : dirs[dir];
  const n = Math.hypot(ux, uy); ux /= n; uy /= n;
  const bx = x - len * ux, by = y - len * uy;
  const px = -uy, py = ux, hw = len * 0.46;
  return `<polygon points="${r1(x)},${r1(y)} ${r1(bx + px * hw)},${r1(by + py * hw)} ${r1(bx - px * hw)},${r1(by - py * hw)}" fill="${fill}"/>`;
}

// Straight arrow: shaft + head, tip exactly at (x2,y2).
export function arrow(x1, y1, x2, y2, o = {}) {
  const { stroke = WIRE, w = 1.5, len = 7 } = o;
  const dx = x2 - x1, dy = y2 - y1, n = Math.hypot(dx, dy);
  const ux = dx / n, uy = dy / n;
  return line(x1, y1, r1(x2 - len * ux), r1(y2 - len * uy), { stroke, w }) +
    head(x2, y2, [ux, uy], { fill: stroke, len });
}

// Small mid-path direction marker (same triangle, sits ON the path).
export function chev(x, y, dir, o = {}) {
  return head(x, y, dir, { fill: o.fill || o.stroke || WIRE, len: 6 });
}

export function box(x, y, w, h, o = {}) {
  const { stroke = BOXLINE, fill = BOXFILL, sw = 1.5, rx = 3, dash = null } = o;
  const d = dash ? ` stroke-dasharray="${dash}"` : '';
  return `<rect x="${x}" y="${y}" width="${w}" height="${h}" rx="${rx}" fill="${fill}" stroke="${stroke}" stroke-width="${sw}"${d}/>`;
}

// Standard frame: title on top, caption lines pinned to the bottom.
export function diagram(w, h, title, body, captions = []) {
  const cap = captions.map((s, i) =>
    txt(w / 2, h - 8 - 12 * (captions.length - 1 - i), s, { fill: DIM, anchor: 'middle' })
  ).join('\n  ');
  return `
<svg class="lesson-diagram" viewBox="0 0 ${w} ${h}" xmlns="http://www.w3.org/2000/svg">
  ${txt(w / 2, 11, title, { fill: DIM, anchor: 'middle' })}
  ${body}
  ${cap}
</svg>`;
}

